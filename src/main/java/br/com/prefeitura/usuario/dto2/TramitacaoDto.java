package br.com.prefeitura.usuario.dto2;

public class TramitacaoDto {
	
	private String unidade;
	private String dtrecebimento;
	private int cdsetor;
	private String setor;
	private String usuariorecebe;
	private int nuano;
	private int nuprocesso;
	private String sgorgaosetor;
	
	
	
	
	
	
	
	public int getNuano() {
		return nuano;
	}
	public void setNuano(int nuano) {
		this.nuano = nuano;
	}
	public int getNuprocesso() {
		return nuprocesso;
	}
	public void setNuprocesso(int nuprocesso) {
		this.nuprocesso = nuprocesso;
	}
	public String getSgorgaosetor() {
		return sgorgaosetor;
	}
	public void setSgorgaosetor(String sgorgaosetor) {
		this.sgorgaosetor = sgorgaosetor;
	}
	public String getUnidade() {
		return unidade;
	}
	public void setUnidade(String unidade) {
		this.unidade = unidade;
	}
	public String getDtrecebimento() {
		return dtrecebimento;
	}
	public void setDtrecebimento(String dtrecebimento) {
		this.dtrecebimento = dtrecebimento;
	}
	public int getCdsetor() {
		return cdsetor;
	}
	public void setCdsetor(int cdsetor) {
		this.cdsetor = cdsetor;
	}
	public String getSetor() {
		return setor;
	}
	public void setSetor(String setor) {
		this.setor = setor;
	}
	public String getUsuariorecebe() {
		return usuariorecebe;
	}
	public void setUsuariorecebe(String usuariorecebe) {
		this.usuariorecebe = usuariorecebe;
	}

	
}
